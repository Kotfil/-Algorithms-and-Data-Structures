class Solution {
    /**
     * @param {number[]} nums
     * @return {number[][]}
     */
    threeSum(nums) {
        const res = [];

        // 1. Обязательный шаг для паттерна "Two Pointers" — сортировка массива
        // Сортируем по возрастанию (от меньшего к большему)
        nums.sort((a, b) => a - b);

        for (let i = 0; i < nums.length - 2; i++) {
            // : если самое маленькое число (текущее) уже больше нуля,
            // то сумма трех положительных чисел никогда не даст 0. Можно смело завершать работу.
            if (nums[i] > 0) break;

            // Пропускаем дубликаты для ПЕРВОГО числа, чтобы не было одинаковых ответов
            if (i > 0 && nums[i] === nums[i - 1]) continue;

            // 2. Классические "Два Указателя" для оставшейся части массива
            let left = i + 1;
            let right = nums.length - 1;

            while (left < right) {
                const sum = nums[i] + nums[left] + nums[right];

                if (sum === 0) {
                    // Ура, нашли тройку! Добавляем в ответ
                    res.push([nums[i], nums[left], nums[right]]);

                    // Сдвигаем оба указателя для поиска следующих возможных пар
                    left++;
                    right--;

                    // Пропускаем дубликаты для ВТОРОГО числа (левого указателя)
                    // (для правого пропускать не обязательно, так как сумма сама заставит его сдвинуться)
                    while (left < right && nums[left] === nums[left - 1]) {
                        left++;
                    }
                } else if (sum < 0) {
                    // Сумма маловата -> сдвигаем левый указатель вправо, чтобы увеличить сумму
                    left++;
                } else {
                    // Сумма великовата -> сдвигаем правый указатель влево, чтобы уменьшить сумму
                    right--;
                }
            }
        }

        return res;
    }
}

