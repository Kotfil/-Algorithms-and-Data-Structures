class Solution {
    /**
     * @param {character[][]} board
     * @return {boolean}
     */
    isValidSudoku(board) {
        // Создаем три массива для памяти: "видели ли мы эту цифру?"
        // Мы используем массивы 9x10 (где второй индекс - это сама цифра от 1 до 9).
        // Это гораздо быстрее, чем использовать встроенный Set().
        const rows = Array.from({ length: 9 }, () => new Array(10).fill(false));
        const cols = Array.from({ length: 9 }, () => new Array(10).fill(false));
        const boxes = Array.from({ length: 9 }, () => new Array(10).fill(false));

        for (let r = 0; r < 9; r++) {
            for (let c = 0; c < 9; c++) {
                const char = board[r][c];
                
                // Пропускаем пустые клетки
                if (char === '.') continue;
                
                // Превращаем символ в число
                const val = parseInt(char, 10);
                
                // Магическая формула: определяем индекс квадрата 3х3 (от 0 до 8)
                // Math.floor(r / 3) дает номер ряда квадратов (0, 1 или 2)
                // Math.floor(c / 3) дает номер колонки квадратов (0, 1 или 2)
                const boxIdx = Math.floor(r / 3) * 3 + Math.floor(c / 3);
                
                // Проверяем: если мы УЖЕ видели эту цифру в этой строке, колонке или квадрате
                if (rows[r][val] || cols[c][val] || boxes[boxIdx][val]) {
                    return false; // Нашли дубликат — Судоку невалидно!
                }
                
                // Если не видели, то отмечаем цифру как "увиденную" (true)
                rows[r][val] = true;
                cols[c][val] = true;
                boxes[boxIdx][val] = true;
            }
        }
        
        // Если прошли всю доску и не нашли ни одного дубликата
        return true;
    }
}

